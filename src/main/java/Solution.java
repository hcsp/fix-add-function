public class Solution {
    public static int add(int a, int b) {
        return a + b;
    }
    public static int decrement(int a, int b) {
        return a - b;
    }

    public static void main(String[] args) {
        System.out.println("1+1=" + add(1, 1));
        System.out.println("1-1=" + decrement(1, 1));
    }
}
